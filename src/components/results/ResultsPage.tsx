import { useNavigate } from 'react-router-dom'
import { usePatientForm } from '../../hooks/usePatientForm'
import BiologicCard from './BiologicCard'
import { BIOLOGICS } from '../../data/biologics'

export default function ResultsPage() {
  const { result, patient, reset } = usePatientForm()
  const navigate = useNavigate()

  if (!result) {
    return (
      <div className="text-center py-16 text-gray-500">
        <p className="mb-4">No recommendation generated yet.</p>
        <button
          type="button"
          onClick={() => navigate('/')}
          className="px-4 py-2 bg-brand-600 text-white rounded text-sm hover:bg-brand-700"
        >
          Go to patient intake
        </button>
      </div>
    )
  }

  return (
    <div className="space-y-8">
      {/* Header */}
      <div className="flex items-center justify-between print:hidden">
        <div>
          <h2 className="text-xl font-bold text-gray-900">Biologic Preselection — CRSwNP</h2>
          <p className="text-sm text-gray-500 mt-0.5">
            Generated {new Date(result.generatedAt).toLocaleString()} · Content v{result.contentVersion}
          </p>
        </div>
        <div className="flex gap-2">
          <button
            type="button"
            onClick={() => window.print()}
            className="px-3 py-1.5 border border-gray-300 rounded text-sm text-gray-700 hover:bg-gray-50"
          >
            Print / Export
          </button>
          <button
            type="button"
            onClick={() => { reset(); navigate('/') }}
            className="px-3 py-1.5 border border-gray-300 rounded text-sm text-gray-700 hover:bg-gray-50"
          >
            New Patient
          </button>
        </div>
      </div>

      {/* Print header */}
      <div className="hidden print:block">
        <h1 className="text-2xl font-bold">BiologicaIndication — CRSwNP Biologic Preselection</h1>
        <p className="text-sm text-gray-600">
          Generated {new Date(result.generatedAt).toLocaleString()} · Content v{result.contentVersion}
        </p>
        <p className="mt-2 text-xs border border-gray-300 rounded p-2 text-gray-600">
          This output is generated by a clinical decision support tool. It is not a certified medical device
          and does not replace physician clinical judgement. All treatment decisions must be made by the
          responsible treating physician.
        </p>
      </div>

      {/* Global warnings */}
      {result.globalWarnings.length > 0 && (
        <div className="space-y-2">
          <h3 className="text-sm font-semibold text-gray-700 uppercase tracking-wide">
            Global Warnings
          </h3>
          {result.globalWarnings.map((w) => (
            <div
              key={w.ruleId}
              className="rounded border border-amber-300 bg-amber-50 p-3 text-sm text-amber-900"
            >
              <span className="font-semibold">Caution: </span>{w.message}
            </div>
          ))}
        </div>
      )}

      {/* Missing data */}
      {result.missingData.length > 0 && (
        <div className="rounded border border-blue-200 bg-blue-50 p-4 space-y-2">
          <h3 className="text-sm font-semibold text-blue-800">Missing Data — Ranking May Be Incomplete</h3>
          <ul className="space-y-1">
            {result.missingData.map((m) => (
              <li key={m.field} className="text-xs text-blue-700">
                <span className="font-medium">{m.field}:</span> {m.impact}
              </li>
            ))}
          </ul>
        </div>
      )}

      {/* Ranked list */}
      {result.ranked.length > 0 ? (
        <div className="space-y-4">
          <h3 className="text-sm font-semibold text-gray-700 uppercase tracking-wide">
            Ranked Shortlist ({result.ranked.length} eligible)
          </h3>
          {result.ranked.map((r) => (
            <BiologicCard key={r.biologicId} result={r} />
          ))}
        </div>
      ) : (
        <div className="rounded border border-gray-200 bg-gray-50 p-6 text-center text-gray-500 text-sm">
          All biologics were excluded based on the entered patient data. Review exclusion reasons below.
        </div>
      )}

      {/* Excluded drugs */}
      {result.excluded.length > 0 && (
        <details className="rounded border border-gray-200">
          <summary className="cursor-pointer px-4 py-3 text-sm font-medium text-gray-700 hover:bg-gray-50">
            Excluded biologics ({result.excluded.length})
          </summary>
          <div className="px-4 pb-4 space-y-3 pt-2">
            {result.excluded.map((e) => {
              const bio = BIOLOGICS.find((b) => b.id === e.biologicId)
              return (
                <div key={e.biologicId} className="text-sm">
                  <p className="font-medium text-gray-800">{bio?.name ?? e.biologicId}</p>
                  <ul className="mt-1 space-y-0.5">
                    {e.reasons.map((r) => (
                      <li key={r.ruleId} className="text-xs text-gray-600">
                        <span className="text-red-500 mr-1">—</span>
                        {r.reason}
                      </li>
                    ))}
                  </ul>
                </div>
              )
            })}
          </div>
        </details>
      )}

      {/* Physician summary (print) */}
      <div className="hidden print:block mt-8 border-t border-gray-300 pt-4">
        <h2 className="text-lg font-bold mb-2">Patient Data Summary</h2>
        <div className="text-xs space-y-1 text-gray-700">
          <p><b>Severity:</b> {patient.clinicalHistory.severity}</p>
          <p><b>Prior ESS:</b> {patient.clinicalHistory.priorESS} · Recurrence: {patient.clinicalHistory.recurrenceAfterESS}</p>
          <p><b>Systemic steroid courses:</b> {patient.clinicalHistory.systemicSteroidCourses ?? 'not documented'}</p>
          <p><b>Blood eos:</b> {patient.biomarkers.bloodEosinophils ?? 'not documented'} cells/µL</p>
          <p><b>Total IgE:</b> {patient.biomarkers.totalIgE ?? 'not documented'} IU/mL</p>
          <p><b>Atopic sensitization:</b> {patient.biomarkers.atopicSensitization}</p>
          <p><b>Asthma:</b> {patient.comorbidities.asthma} · N-ERD: {patient.comorbidities.nsaidErd}</p>
          <p><b>Atopic dermatitis:</b> {patient.comorbidities.atopicDermatitis}</p>
          <p><b>Region:</b> {patient.practicalFactors.region} · Dosing preference: {patient.practicalFactors.dosingPreference}</p>
          <p><b>Pregnancy planning:</b> {patient.practicalFactors.pregnancyPlanning}</p>
        </div>
      </div>

      {/* Disclaimer footer */}
      <div className="text-xs text-gray-400 text-center border-t border-gray-100 pt-4">
        This shortlist is generated by an explainable rule engine for clinical decision support only.
        It is not a diagnostic device. Final treatment decisions rest with the treating physician.
        Always verify regulatory approval status and drug label before prescribing.
      </div>
    </div>
  )
}
